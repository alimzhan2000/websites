	Ее цель заключается в создании мобильных диагностических устройств, расположенных преимущественно в аптеках и поликлиниках города Алматы. Устройство состоит из умной перчатки и браслета, использующие электропунктурную и пульсовую диагностику и, определив диагноз, отправляют его прямиком к специализированным врачам компании. Те, в свою очередь, сверяют данные и за 5 минут отправляют конечный результат семейному врачу клиента и в аппарат, находящийся рядом с самим устройством, чтобы человек мог сразу на экране увидеть свой диагноз.После, этот диагноз сразу удаляется (медицинская конфиденциальность). Быстрым, доступным и надежным методом, компания помогает людям следить за своим здоровьем, не тратя целый день на сдачу анализов и в переполненных кабинетах врачей.
	Говоря о деталях пользования, можно определить несколько этапов. Первое, клиент должен прийти в аптеку или поликлинику, которая приобрела устройство “ЦиВита”. Второе, клиент должен ввести данные своего ИИН в аппарат, находящийся рядом с устройством. Третье, он должен надеть перчатку и браслет, следуя инструкции, которая находится на экране аппарата. Четвертое, подождать пять минут, пока устройство определить диагноз и еще 5 минут на то, чтобы данные обработали врачи. Пройдя все этапы, диагноз отобразится на экране аппарата и человек, уведомленный о наличии отклонений, может обратиться к семейному врачу, который также получил его диагноз и начать свое лечение.</p><p style="font-size: 20px; line-height: 2;text-align: justify; text-indent: 40px; margin:left: 20px;margin-right: 20px">
